CREATE PROCEDURE  CHECK_LOGIN(IN TAI_KHOAN VARCHAR(30) CHARACTER SET UTF8, IN MAT_KHAU LONGTEXT)
BEGIN 
    SELECT NHAN_VIEN.MA_NHAN_VIEN, 
           NHAN_VIEN.HO_TEN_NHAN_VIEN, 
           NHAN_VIEN.EMAIL_NHAN_VIEN, 
           NHAN_VIEN.TAI_KHOAN_NHAN_VIEN, 
           NHAN_VIEN.MA_QUYEN_NHAN_VIEN,
           QUYEN_NHAN_VIEN.TEN_QUYEN_NHAN_VIEN
    FROM NHAN_VIEN INNER JOIN QUYEN_NHAN_VIEN ON NHAN_VIEN. MA_QUYEN_NHAN_VIEN = QUYEN_NHAN_VIEN.MA_QUYEN_NHAN_VIEN
    WHERE NHAN_VIEN.TAI_KHOAN_NHAN_VIEN = TAI_KHOAN AND NHAN_VIEN.MAT_KHAU_NHAN_VIEN = MD5(MAT_KHAU);
END $$
