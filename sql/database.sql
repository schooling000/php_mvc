CREATE PROCEDURE  GET_PERMISSIONS(IN ROLE_ID VARCHAR(30) CHARACTER SET UTF8)
BEGIN 
    SELECT  MA_NHIEM_VU_NHAN_VIEN ,
            TEN_NHIEM_VU_NHAN_VIEN,
            DUOC_PHEP_XEM,
            DUOC_PHEP_THEM,        
            DUOC_PHEP_SUA,         
            DUOC_PHEP_XOA,         
            DUOC_PHEP_MO_MODAL_SUA,
            DUOC_PHEP_MO_MODAL_XOA,
            HREF
    FROM NHIEM_VU_NHAN_VIEN 
    WHERE MA_QUYEN_NHAN_VIEN = ROLE_ID;
END $$
