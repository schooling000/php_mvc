CREATE DATABASE IF NOT EXISTS KHOA_NGOAI DEFAULT CHARACTER SET UTF8 COLLATE UTF8_GENERAL_CI;

USE KHOA_NGOAI;

CREATE TABLE IF NOT EXISTS QUYEN_NHAN_VIEN(
    MA_QUYEN_NHAN_VIEN     VARCHAR(30) NOT NULL PRIMARY KEY,
    TEN_QUYEN_NHAN_VIEN    VARCHAR(50)
) ENGINE = INNODB DEFAULT CHARACTER SET UTF8 COLLATE UTF8_GENERAL_CI; 

CREATE TABLE IF NOT EXISTS NHIEM_VU_NHAN_VIEN(
    ID INT(3) NOT NULl AUTO_INCREMENT PRIMARY KEY,
    MA_QUYEN_NHAN_VIEN      VARCHAR(30) NOT NULL,
    MA_NHIEM_VU_NHAN_VIEN   VARCHAR(30) NOT NULL,
    TEN_NHIEM_VU_NHAN_VIEN  VARCHAR(50) NOT NULL,
    DUOC_PHEP_XEM           ENUM('TRUE','FALSE') DEFAULT 'TRUE',
    DUOC_PHEP_THEM          ENUM('TRUE','FALSE') DEFAULT 'TRUE',
    DUOC_PHEP_SUA           ENUM('TRUE','FALSE') DEFAULT 'TRUE',
    DUOC_PHEP_XOA           ENUM('TRUE','FALSE') DEFAULT 'TRUE',
    DUOC_PHEP_MO_MODAL_SUA  ENUM('TRUE','FALSE') DEFAULT 'TRUE',
    DUOC_PHEP_MO_MODAL_XOA  ENUM('TRUE','FALSE') DEFAULT 'TRUE',
    HREF LONGTEXT DEFAULT 'index.php?controller=&method=',
    FOREIGN KEY(MA_QUYEN_NHAN_VIEN) REFERENCES QUYEN_NHAN_VIEN(MA_QUYEN_NHAN_VIEN)
) ENGINE = INNODB DEFAULT CHARACTER SET UTF8 COLLATE UTF8_GENERAL_CI; 

CREATE TABLE IF NOT EXISTS NHAN_VIEN(
    MA_NHAN_VIEN INT(2) NOT NULL AUTO_INCREMENT PRIMARY KEY,
    HO_TEN_NHAN_VIEN VARCHAR(30) NOT NULL,
    EMAIL_NHAN_VIEN VARCHAR(30) NOT NULL,
    TAI_KHOAN_NHAN_VIEN VARCHAR(30) NOT NULL,
    MAT_KHAU_NHAN_VIEN LONGTEXT NOT NULL,
    MA_QUYEN_NHAN_VIEN VARCHAR(30) NOT NULL,
    UNIQUE(HO_TEN_NHAN_VIEN, EMAIL_NHAN_VIEN, TAI_KHOAN_NHAN_VIEN),
    FOREIGN KEY(MA_QUYEN_NHAN_VIEN) REFERENCES QUYEN_NHAN_VIEN(MA_QUYEN_NHAN_VIEN)
) ENGINE = INNODB DEFAULT CHARACTER SET UTF8 COLLATE UTF8_GENERAL_CI; 



