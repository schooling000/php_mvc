CREATE DATABASE DNGOAI_KHOA;

-- TẠO BẢNG NHÂN VIÊN
CREATE TABLE TNHAN_VIEN( 
    FMA_NHAN_VIEN INT(3) NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    FHO_TEN_NHAN_VIEN VARCHAR(30) CHARACTER SET utf8 COLLATE utf8_general_ci NOT NULL , 
    FTAI_KHOAN_NHAN_VIEN VARCHAR(30) CHARACTER SET utf8 COLLATE utf8_general_ci NOT NULL , 
    FMAT_KHAU_NHAN_VIEN VARCHAR(30) CHARACTER SET utf8 COLLATE utf8_general_ci NOT NULL , 
    FMA_QUYEN_NHAN_VIEN INT(2) NOT NULL
) ENGINE = InnoDB;

-- TẠO BẢNG QUYỀN NHÂN VIÊN
CREATE TABLE TQUYEN_NHAN_VIEN( 
    FQUYEN_NHAN_VIEN INT(3) NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    FQUYEN_NHAN_VIEN VARCHAR(30) CHARACTER SET utf8 COLLATE utf8_general_ci NOT NULL
) ENGINE = InnoDB;

